<b>D2U Kurse Addon</b>

<br /><br />
<p>Komfortable Verwaltung von Kursangeboten. Mit einem Plugin können auch
	aus der Software Kufer per XML Kurse importiert werden.</p>
<p>DSGVO Hinweis: Dieses Addon verfügt über einen Warenkorb. Wenn dieser genutzt
	wird muss ein Cookie Hinweis angezeigt werden. Im Cookie werden die Informationen
	im Warenkorb gespeichert.</p>