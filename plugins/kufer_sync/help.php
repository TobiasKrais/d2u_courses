<b>D2U Kurse Addon - Kufer Sync Plugin</b>

<br /><br />
<p>Kufer SQL Software kann einen XML Export erstellen und per FTP auf einen
	Webserver hochladen. Hierfür ist neben der Kufer Lizenz auch noch eine
	Lizenz für den XML Export nötig.</p>
<p>Dieses Plugin kann die exportierte XML Datei auslesen und in das D2U Kurse
	Addon einlesen. Je nach Einstellung kann das automatisch per CronJob Addon
	durchgeführt werden.</p>
<p>Für Anmeldungen der importierten Kurse werden XML Anmeldungen generiert, die
	Kufer wieder importieren kann.</p>